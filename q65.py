def spv(str1):
    list1 = str1.split()
    numb = []
    for i in range(1,len(list1)):
        numb.append(int(list1[i]))

    avg = float(sum(numb)/len(numb))
    comm = avg/100
    c = 0
    for u in numb:
        c += (avg-u)**2
    sd = (c/len(numb))**(1/2)
    if sd > 4*comm:
        print(list1[0])
        print(" ")

